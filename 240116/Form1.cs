        void Program_KeyDown(object sender, KeyEventArgs e)
        {
            switch (e.KeyData)
            {
                case Keys.W:
                    isDownW = true;
                    break;
                case Keys.A:
                    isDownA = true;
                    isDownD = false;
                    break;
                case Keys.D:
                    isDownD = true;
                    isDownA = false;
                    break;
                case Keys.Escape:
                    Application.Exit();
                    break;
            }
        }

        void Program_KeyUp(object sender, KeyEventArgs e)
        {
            switch (e.KeyData)
            {
                case Keys.W:
                    isDownW = false;
                    break;
                case Keys.A:
                    isDownA = false;
                    break;
                case Keys.D:
                    isDownD = false;
                    break;
                case Keys.C:
                    if (reSizeEnabled)
                        reSizeEnabled = false;
                    else 
                        reSizeEnabled = true;
                    break;
                case Keys.B:
                    if (debugInfo)
                        debugInfo = false;
                    else
                        debugInfo = true;
                    break;
                case Keys.D1:
                    if (gameSize > 0.5)
                        gameSize -= 0.25f;
                    break;
                case Keys.D2:
                    if (gameSize < 1) 
                        gameSize += 0.25f;
                    break;
                case Keys.X:
                    if (paused && !gameover)
                        paused = false;
                    else
                        paused = true;
                    break;
                case Keys.Enter:
                    message = false;
                    Cursor.Hide();
                    break;
                case Keys.Space:
                    if (is2D)
                        is2D = false;
                    else is2D = true;
                    break;
            }
        }

        private bool heroCanFall()
        {
            int newY, newX, newX2, map1, map2;
            newY = (int)((heroY - (is2D ? 12 : 9) + (heroVelocity > 0 ? HERO_HEIGHT : -11)) / BLOCK_SIZE);
            newX = (int)((heroX - 15) / BLOCK_SIZE);
            newX2 = (int)((heroX + HERO_WIDTH - 15) / BLOCK_SIZE);
            map1 = map[currentMap, newY, newX];
            map2 = map[currentMap, newY, newX2];
            if (map1 == 2 && heroVelocity > 0)
                map[currentMap, newY, newX] = 0; 
            if (map2 == 2 && heroVelocity > 0)
                map[currentMap, newY, newX2] = 0;
            if (map1 == 0 && map2 == 0)
                return true;
            if (map1 >= 30 && map1 <= 30 + CHECKPOINTS_AT_LEVEL)
            {
                currentCheckPoint = map1 - 30;
                map[currentMap, newY, newX] = 0;
                return true;
            }
            if (map2 >= 30 && map2 <= 30 + CHECKPOINTS_AT_LEVEL)
            {
                currentCheckPoint = map2 - 30;
                map[currentMap, newY, newX2] = 0;
                return true;
            }
            if (
                (map1 >= 10 && map1 <= 13) ||
                (map2 >= 10 && map2 <= 13)  )
            {
                heroReset();
                return false;
            }
            if (
                (map1 >= 60 && map1 <= 69) ||
                (map2 >= 60 && map2 <= 69)  )
                    PortalTeleportation(newY, newX);
            if (map1 == 40 || map2 == 40)
                return takeSuperBoots(newY, newX);
            if (map1 == 5 || map2 == 5)
                return takeKeyAndCanPath(newY, newX);
            return false;
        }

